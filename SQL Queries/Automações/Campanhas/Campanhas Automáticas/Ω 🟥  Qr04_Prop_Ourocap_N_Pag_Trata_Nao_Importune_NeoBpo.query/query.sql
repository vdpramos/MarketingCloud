SELECT
	A.CPF_CNPJ AS ID,
	CASE WHEN A.CAMPANHA_NAO_IMPORTUNE = 1 AND (N1.TELEFONE IS NOT NULL OR N2.TELEFONE IS NOT NULL OR N3.TELEFONE IS NOT NULL OR N4.TELEFONE IS NOT NULL OR N5.TELEFONE IS NOT NULL) THEN 1 ELSE 0 END AS NAO_IMPORTUNE
FROM Tb_Base_Prop_Ourocap_N_Pag A
LEFT JOIN TB_BASE_NAO_IMPORTUNE N1 ON ISNULL(CONCAT(A.DDD_TEL_01, A.TEL_01), '00000000000') = LTRIM(RTRIM(N1.TELEFONE))
LEFT JOIN TB_BASE_NAO_IMPORTUNE N2 ON ISNULL(CONCAT(A.DDD_TEL_02, A.TEL_02), '00000000000') = LTRIM(RTRIM(N2.TELEFONE))
LEFT JOIN TB_BASE_NAO_IMPORTUNE N3 ON ISNULL(CONCAT(A.DDD_TEL_03, A.TEL_03), '00000000000') = LTRIM(RTRIM(N3.TELEFONE))
LEFT JOIN TB_BASE_NAO_IMPORTUNE N4 ON ISNULL(CONCAT(A.DDD_TEL_04, A.TEL_04), '00000000000') = LTRIM(RTRIM(N4.TELEFONE))
LEFT JOIN TB_BASE_NAO_IMPORTUNE N5 ON ISNULL(CONCAT(A.DDD_TEL_05, A.TEL_05), '00000000000') = LTRIM(RTRIM(N5.TELEFONE))
LEFT JOIN TB_BASE_NAO_IMPORTUNE N6 ON ISNULL(CONCAT(A.DDD_TEL_06, A.TEL_06), '00000000000') = LTRIM(RTRIM(N6.TELEFONE))