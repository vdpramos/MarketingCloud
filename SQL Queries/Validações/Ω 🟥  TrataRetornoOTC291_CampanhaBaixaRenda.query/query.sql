SELECT
  A.CODIGO_PARCEIRO,
  A.ID_CLIENTE,
  A.CODIGO_MCI,
  A.TIPO_PESSOA,
  A.CODIGO_CIC,
  A.DT_NASCIMENTO,
  A.NOME,
  A.UF_RESIDENCIAL,
  A.DDD1,
  A.TELEFONE1,
  A.DDD2,
  A.TELEFONE2,
  A.DDD3,
  A.TELEFONE3,
  A.CODIGO_PLANO,
  A.ORIGEM_SOLICITACAO,
  A.FILLER,
  A.CODIGO_RNA,
  A.VALOR_RENDIMENTOS,
  A.TIPO_CARTEIRA,
  A.NIVEL_RELACIONAMENTO,
  A.DATA_INFORMACAO,
  A.FILLER2,
  B.CPF AS CPF,
  Left(B.NOME_COMPLETO, CharIndex(' ', B.NOME_COMPLETO)) AS PRIMEIRO_NOME,
  Reverse(Left(Reverse(B.Nome_COMPLETO), CharIndex(' ', Reverse(B.Nome_COMPLETO)))) AS SOBRENOME,
  DDD_Fone_01 AS TEL_RESIDENCIAL,
  B.DDD_Fone_02 AS TEL_COMERCIAL,
  B.DDD_Fone_03 AS CELULAR1,
  B.DDD_Fone_04,
  B.DDD_Fone_05,
  A.DT_NASCIMENTO_SF,
  B.SEXO,
  B.PUBLICO,
  CONCAT(B.CPF, 'Cliente') AS CPF_TIPO_RELACIONAMENTO,
  CONCAT(B.CPF, '52') AS CPF_CODIGO_PARCEIRO,A.ALTA_RENDA
FROM
  TB_BASE_OTC291_TRATADA A /* TB_BASE_OTC291 */
INNER JOIN PublicoInicialCampBaixaRenda B ON A.CPF = B.CPF