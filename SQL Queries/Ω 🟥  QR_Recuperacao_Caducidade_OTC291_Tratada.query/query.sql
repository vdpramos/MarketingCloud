SELECT
  A.CODIGO_PARCEIRO,
  A.ID_CLIENTE,
  A.CODIGO_MCI,
  A.TIPO_PESSOA,
  A.CODIGO_CIC,
  A.DT_NASCIMENTO,
  A.NOME,
  A.SEXO,
  A.UF_RESIDENCIAL,
  A.DDD1,
  A.TELEFONE1,
  A.DDD2,
  A.TELEFONE2,
  A.DDD3,
  A.TELEFONE3,
  A.CODIGO_PLANO,
  A.ORIGEM_SOLICITACAO,
  A.FILLER,
  A.CODIGO_RNA,
  A.VALOR_RENDIMENTOS,
  A.TIPO_CARTEIRA,
  A.NIVEL_RELACIONAMENTO,
  A.DATA_INFORMACAO,
  A.FILLER2,
  B.CPF_CNPJ AS CPF,
  LEFT(UPPER(SUBSTRING(B.NOME, 1, CHARINDEX(' ', B.NOME) - 1)), 50) AS PRIMEIRO_NOME,
  LEFT(UPPER(SUBSTRING(B.NOME, CHARINDEX(' ', B.NOME) + 1, LEN(B.NOME))), 50) AS SOBRENOME,
  A.DT_NASCIMENTO_SF,
  CONCAT(B.CPF_CNPJ,'Cliente') AS CPF_TIPO_RELACIONAMENTO,
  CONCAT(B.CPF_CNPJ,'52') AS CPF_CNPJ_CODIGO_DO_PARCEIRO,
  B.ID_CLIENTE_PARCEIRO,
  B.ID_CONTATO,
  B.ID_CAMPANHA,
  /* CONCAT(CONVERT(INT, SUBSTRING(RETORNO, 234, 13)),'.',RIGHT(SUBSTRING(RETORNO, 234, 15), 2)) AS FAIXA_RENDA_MENSAL, */
  A.Alta_Renda
FROM Tb_Recuperacao_Caducidade_Tratada B
INNER JOIN TB_BASE_OTC291_TRATADA A ON A.CPF = B.CPF_CNPJ